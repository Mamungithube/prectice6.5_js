const number = 36;
if(number >= 80){
    console.log(" You got A+");
}
else if(number >= 70 && number <= 79){
    console.log(" You got A");
}
else if(number >= 60 && number <= 69){
    console.log(" You got A-");
}
else if(number >= 50 && number <= 59){
    console.log(" You got B");
}
else if(number >= 40 && number <= 49){
    console.log(" You got c");
}
else{
    console.log("You got F");
}